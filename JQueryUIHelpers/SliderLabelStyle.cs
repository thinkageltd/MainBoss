
namespace JQueryUIHelpers
{
    /// <summary>
    /// Defines the label styles for slider widgets.
    /// </summary>
    public enum SliderLabelStyle
    {
        /// <summary>
        /// No label.
        /// </summary>
        None,

        /// <summary>
        /// Displays the value.
        /// </summary>
        ValueOnly,

        /// <summary>
        /// Displays both label and value.
        /// </summary>
        LabelAndValue
    }
}
