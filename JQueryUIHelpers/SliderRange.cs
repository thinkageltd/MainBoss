namespace JQueryUIHelpers
{
    /// <summary>
    /// Specifies the type of the slider range.
    /// </summary>
    public enum SliderRange
    {
        /// <summary>
        /// A range defined by the minimum and a single value.
        /// </summary>
        Min,

        /// <summary>
        /// A range defined by the maximum and a single value.
        /// </summary>
        Max,

        /// <summary>
        /// A range defined by two values.
        /// </summary>
        Between
    }
}
