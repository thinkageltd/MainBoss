namespace JQueryUIHelpers
{
    /// <summary>
    /// Specifies a selectable tolerance.
    /// </summary>
    public enum SelectableTolerance
    {
        /// <summary>
        /// The dragging box (lasso) overlaps the selectee any amount.
        /// </summary>
        Touch,

        /// <summary>
        /// The dragging box (lasso) overlaps the selectee entirely.
        /// </summary>
        Fit
    }
}
