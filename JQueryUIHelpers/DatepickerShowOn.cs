namespace JQueryUIHelpers
{
    /// <summary>
    /// Specifies the event that triggers the datepicker.
    /// </summary>
    public enum DatepickerShowOn
    {
        /// <summary>
        /// Show on focus.
        /// </summary>
        Focus,

        /// <summary>
        /// Show on button click.
        /// </summary>
        Button,

        /// <summary>
        /// Show on focus or button click.
        /// </summary>
        Both
    }
}
