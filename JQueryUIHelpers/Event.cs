namespace JQueryUIHelpers
{
    /// <summary>
    /// Specifies an event.
    /// </summary>
    public enum Event
    {
        /// <summary>
        /// Click Event.
        /// </summary>
        Click,

        /// <summary>
        /// Double click Event.
        /// </summary>
        DblClick,

        /// <summary>
        /// Mouse enter Event.
        /// </summary>
        MouseEnter,

        /// <summary>
        /// Mouse over Event.
        /// </summary>
        MouseOver
    }
}
