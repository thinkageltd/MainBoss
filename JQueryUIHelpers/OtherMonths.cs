namespace JQueryUIHelpers
{
    /// <summary>
    /// Specifies how the datepicker should handle the days in other months.
    /// </summary>
    public enum OtherMonths
    {
        /// <summary>
        /// Dates in other months are hidden.
        /// </summary>
        Hidden,

        /// <summary>
        /// Dates in other months are displayed but not selectable.
        /// </summary>
        Visible,

        /// <summary>
        /// Dates in other months are displayed and selectable.
        /// </summary>
        VisibleAndSelectable
    }
}
