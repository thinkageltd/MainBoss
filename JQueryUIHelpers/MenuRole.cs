namespace JQueryUIHelpers
{
    /// <summary>
    /// Defines the ARIA roles for the menu
    /// </summary>
    public enum MenuRole
    {
        /// <summary>
        /// Set No roles.
        /// </summary>
        None,

        /// <summary>
        /// Set the menu item's role to 'menuitem'.
        /// </summary>
        Menu,

        /// <summary>
        /// Set the menu item's role to 'option'.
        /// </summary>
        Listbox
    }
}
