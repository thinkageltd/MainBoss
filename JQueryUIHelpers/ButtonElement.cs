namespace JQueryUIHelpers
{
    /// <summary>
    /// Specifies which element should be used to create the button.
    /// </summary>
    public enum ButtonElement
    {
        /// <summary>
        /// The button widget is based on a button element.
        /// </summary>
        Button,

        /// <summary>
        /// The button widget is based on an input element.
        /// </summary>
        Input
    }
}
