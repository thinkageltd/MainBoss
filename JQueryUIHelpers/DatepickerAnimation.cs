        /// </summary>
        None,

        /// <summary>
        /// DatePicker Animation is Show.
        /// </summary>
        Show,

        /// <summary>
        /// DatePicker Animation is SlideDown.
        /// </summary>
        SlideDown,

        /// <summary>
        /// DatePicker Animation is FadeIn.
        /// </summary>
        FadeIn,

        /// <summary>
        /// DatePicker Animation is Blind.
        /// </summary>
        Blind,

        /// <summary>
        /// DatePicker Animation is Clip.
        /// </summary>
        Clip,

        /// <summary>
        /// DatePicker Animation is Drop.
        /// </summary>
        Drop,

        /// <summary>
        /// DatePicker Animation is Explode.
        /// </summary>
        Explode,

        /// <summary>
        /// DatePicker Animation is Fade.
        /// </summary>
        Fade,
        
        /// <summary>
        /// DatePicker Animation is Fold.
        /// </summary>
        Fold,

        /// <summary>
        /// DatePicker Animation is Puff.
        /// </summary>
        Puff,

        /// <summary>
        /// DatePicker Animation is Slide.
        /// </summary>
        Slide,

        /// <summary>
        /// DatePicker Animation is Scale.
        /// </summary>
        Scale,

        /// <summary>
        /// DatePicker Animation is Bounce.
        /// </summary>
        Bounce
    }
}
